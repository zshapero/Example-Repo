// "Estimated daily earnings" = a rough heuristic for the affiliate, assuming
// they capture ~0.3% of today's sales volume on a single video. This is a
// motivational illustration only — real earnings vary wildly.
export function estimateDailyEarnings(product) {
  const capturedUnits = Math.max(1, Math.round(product.unitsSoldToday * 0.003));
  return capturedUnits * product.price * product.commissionRate;
}
